package com.dmw.eteachswayam.exo.activity;

public class Topic {

	private String name;
	private String link;
	public
    String getName() {
		return name;
	}

	public Topic( String name, String link) {
		super();
		this.name = name;
		this.link = link;
	}

	public void setName(String name) {
		this.name = name;
	}
	public
    String getLink() {
		return link;
	}
	public void setLink(String link) {
		this.link = link;
	}

}
